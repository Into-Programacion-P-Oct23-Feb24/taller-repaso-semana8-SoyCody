package ejercicio01;


public class Ejercicio01 {

    public static void main(String[] args) {
        int numero;
        numero = 0;
        boolean bandera;
        bandera = false;
        do {
            System.out.println(numero);
            numero = numero - 10;
        } while (bandera);
        System.out.println(numero);
    }
    
}
